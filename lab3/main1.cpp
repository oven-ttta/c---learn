#include <iostream>

using namespace std;

int main() {
	int num1, num2;
	cin >> num1;
	cin >> num2;

	if (num1 % 2 == 0) {
		cout << "Even" << endl;
	} else {
		cout << "Odd" << endl;
	}

	if (num2 % 2 == 0) {
		cout << "Even" << endl;
	} else {
		cout << "Odd" << endl;
	}

	return 0;
}